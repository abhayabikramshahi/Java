public class InfiniteLoop{
    public static void main(String[] args){
        for (;;) {
            System.out.println("This is an infinite loop.");
            // To stop the loop, you can use Ctrl+C in the console.
            // Or you can add a break condition to exit the loop.
            // Uncomment the next line to break after one iteration.
            // break;
        }
    }
}